const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: [true, 'Name is required'],
    trim: true,
    minlength: [3, 'Name must be at least 3 characters'],
    maxlength: [50, 'Name cannot exceed 50 characters']
  },
  email: {
    type: String,
    required: [true, 'Email is required'],
    unique: true,
    lowercase: true,
    trim: true,
    match: [/^\S+@\S+\.\S+$/, 'Please provide a valid email address']
  },
  password: {
    type: String,
    required: [true, 'Password is required'],
    minlength: [6, 'Password must be at least 6 characters'],
    select: false // Don't include password in queries by default
  },
  avatar: {
    type: String,
    default: 'https://ui-avatars.com/api/?background=4F46E5&color=fff&name='
  },
  preferences: {
    favoriteCuisines: {
      type: [String],
      default: []
    },
    priceRange: {
      type: String,
      enum: ['₹', '₹₹', '₹₹₹', ''],
      default: ''
    },
    dietaryRestrictions: {
      type: [String],
      default: []
    },
    excludeIngredients: {
      type: [String],
      default: []
    }
  },
  favoriteRestaurants: [{
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Restaurant'
  }],
  searchHistory: [{
    query: {
      type: String,
      required: true
    },
    timestamp: {
      type: Date,
      default: Date.now
    },
    resultCount: {
      type: Number,
      default: 0
    },
    clickedRestaurants: [{
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Restaurant'
    }]
  }],
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});
userSchema.pre('save', async function() {
  this.updatedAt = Date.now();
  if (!this.avatar || this.avatar === 'https://ui-avatars.com/api/?background=4F46E5&color=fff&name=') {
    this.avatar = `https://ui-avatars.com/api/?background=4F46E5&color=fff&name=${encodeURIComponent(this.name)}`;
  }
  if (this.isModified('password')) {
    const salt = await bcrypt.genSalt(10);
    this.password = await bcrypt.hash(this.password, salt);
  }
});
userSchema.methods.comparePassword = async function(candidatePassword) {
  try {
    return await bcrypt.compare(candidatePassword, this.password);
  } catch (error) {
    throw new Error('Password comparison failed');
  }
};
userSchema.methods.toJSON = function() {
  const user = this.toObject();
  delete user.password;
  delete user.__v;
  return user;
};
const User = mongoose.model('User', userSchema);
module.exports = User;
